
-- Custom 
local FAUX_LEFT_SHOULDER = script:GetCustomProperty("FauxLeftShoulder"):WaitForObject()
local FUAX_RIGHT_SHOULDER = script:GetCustomProperty("FuaxRightShoulder"):WaitForObject()
local FAUX_LEFT_ELBOW = script:GetCustomProperty("FauxLeftElbow"):WaitForObject()
local FUAX_RIGHT_ELBOW = script:GetCustomProperty("FuaxRightElbow"):WaitForObject()
local FAUX_LEFT_WRIST = script:GetCustomProperty("FauxLeftWrist"):WaitForObject()
local FUAX_RIGHT_WRIST = script:GetCustomProperty("FuaxRightWrist"):WaitForObject()
local LEFT_SHOULDER = script:GetCustomProperty("left_shoulder"):WaitForObject()
local RIGHT_SHOULDER = script:GetCustomProperty("right_shoulder"):WaitForObject()
local LEFT_ELBOW = script:GetCustomProperty("left_elbow"):WaitForObject()
local RIGHT_ELBOW = script:GetCustomProperty("right_elbow"):WaitForObject()
local LEFT_WRIST = script:GetCustomProperty("left_wrist"):WaitForObject()
local RIGHT_WRIST = script:GetCustomProperty("right_wrist"):WaitForObject()
local LEFT_ELBOW_TARGET = script:GetCustomProperty("LeftElbowTarget"):WaitForObject()
local RIGHT_ELBOW_TARGET = script:GetCustomProperty("RightElbowTarget"):WaitForObject()
local LEFT_WRIST_TARGET = script:GetCustomProperty("LeftWristTarget"):WaitForObject()
local RIGHT_WRIST_TARGET = script:GetCustomProperty("RightWristTarget"):WaitForObject()
local LEFT_SHOULDER_TARGET = script:GetCustomProperty("LeftShoulderTarget"):WaitForObject()
local RIGHT_SHOULDER_TARGET = script:GetCustomProperty("RightShoulderTarget"):WaitForObject()

function Tick()
    local leftShoulderRot = LEFT_SHOULDER:GetWorldRotation()
    local rightShoulderRot = RIGHT_SHOULDER:GetWorldRotation()
    local leftElbowRot = LEFT_ELBOW:GetWorldRotation()
    local rightElbowRot = RIGHT_ELBOW:GetWorldRotation()
    local leftWristRot = LEFT_WRIST:GetWorldRotation()
    local rightWristRot = RIGHT_WRIST:GetWorldRotation()

    FAUX_LEFT_SHOULDER:SetWorldRotation(leftShoulderRot)
    FUAX_RIGHT_SHOULDER:SetWorldRotation(rightShoulderRot)
    FAUX_LEFT_ELBOW:SetWorldRotation(leftElbowRot)
    FUAX_RIGHT_ELBOW:SetWorldRotation(rightElbowRot)
    FAUX_LEFT_WRIST:SetWorldRotation(leftWristRot)
    FUAX_RIGHT_WRIST:SetWorldRotation(rightWristRot)
    FAUX_LEFT_ELBOW:SetWorldPosition(LEFT_ELBOW_TARGET:GetWorldPosition())
    FUAX_RIGHT_ELBOW:SetWorldPosition(RIGHT_ELBOW_TARGET:GetWorldPosition())
    FAUX_LEFT_WRIST:SetWorldPosition(LEFT_WRIST_TARGET:GetWorldPosition())
    FUAX_RIGHT_WRIST:SetWorldPosition(RIGHT_WRIST_TARGET:GetWorldPosition())
    FAUX_LEFT_SHOULDER:SetWorldPosition(LEFT_SHOULDER_TARGET:GetWorldPosition())
    FUAX_RIGHT_SHOULDER:SetWorldPosition(RIGHT_SHOULDER_TARGET:GetWorldPosition())
end